function trocaCor1() {
    var botaoDoCoracao = document.getElementById('heart1');
    if (botaoDoCoracao.style.color == "red"){
        botaoDoCoracao.style.color = "black"
    } else {
        botaoDoCoracao.style.color = "red"
    }
}

